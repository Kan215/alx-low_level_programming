#include <stdio.h>
/**
 * main - Entry point of the program
 * Description:a program that prints the alphabet in lowercase in reverse
 * followed by a new line.
 * Return: Always 0 (Success)
 */

int main(void)
{
	char x;

	x = 'z';
	while
		(x >= 'a') {
			putchar(x);
			x--;

		}
	putchar('\n');
	return (0);
}
